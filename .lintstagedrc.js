export default {
  "**/*.{js,jsx,ts,tsx,json,yml,yaml}": (files) => {
    // Exclude lockfiles: they are machine-generated and must not be reformatted
    // by prettier (doing so produces a massive, churny diff). A bare
    // `"!**/pnpm-lock.yaml": []` key does NOT exclude them from this glob —
    // lint-staged matches each key independently — so filter them out here.
    const lockfile = /(?:^|\/)(pnpm-lock\.yaml|package-lock\.json|yarn\.lock)$/;
    const formatable = files.filter((file) => !lockfile.test(file));
    if (formatable.length === 0) return [];
    return [
      `prettier --write ${formatable.map((f) => JSON.stringify(f)).join(" ")}`,
    ];
  },
  "**/*.md": (files) => {
    // Filter out changeset and CHANGELOG files - they need special handling
    // Skip 11ty page templates so YAML frontmatter (---) is not reformatted by remark
    // Filter out files that should not be processed by remark:
    // - changeset and CHANGELOG files - they need special handling
    // - docs/s2-docs files - they have custom YAML frontmatter that remark breaks
    // - docs/markdown/ - generated files, regenerated by markdown-generator
    const processableFiles = files.filter(
      (file) =>
        !file.includes(".changeset/") &&
        !file.includes("CHANGELOG.md") &&
        !file.includes("docs/site/src/") &&
        !file.includes("docs/s2-docs/") &&
        !file.includes("docs/markdown/"),
    );
    if (processableFiles.length === 0) return [];
    // Use -o flag (no path) to write back to same file
    return processableFiles.map(
      (file) =>
        `remark ${file} --use remark-frontmatter --use remark-gfm --use remark-github -o`,
    );
  },
  "sdk/**/*.rs": () => {
    // Use `cargo fmt --all` (not `rustfmt <file>` directly) so formatting is
    // consistent with the sdk:fmt-check CI gate. Calling rustfmt on individual
    // files produces subtly different output (e.g. different line-break decisions)
    // because it lacks the crate-context that cargo provides.
    return ["cargo fmt --manifest-path sdk/Cargo.toml --all"];
  },
  ".changeset/*.md": (files) => {
    // Only run changeset linter on actual changeset files, not README.md
    const changesetFiles = files.filter((file) => !file.endsWith("README.md"));
    if (changesetFiles.length === 0) return [];
    // Use `node` + CLI path so pre-commit does not invoke `pnpm` (avoids Corepack
    // signature/download failures when `pnpm` triggers a tarball download).
    // lint-staged function tasks must return string | string[] (not argv tuples).
    return changesetFiles.map(
      (file) =>
        `node tools/changeset-linter/src/cli.js check-file ${JSON.stringify(file)}`,
    );
  },
};
