//! The [`Dataset`] class — the primary JS-facing object for token operations.
//!
//! # Data loading
//!
//! - [`Dataset::embedded()`] — opens the canonical Spectrum token dataset from a
//!   prebuilt `.redb` cache blob embedded in this wasm binary at compile time.
//! - [`Dataset::from_tokens()`] — builds a `TokenGraph` from token JSON passed in
//!   from JavaScript (e.g. loaded from `@adobe/spectrum-design-data`).

use std::collections::HashSet;
#[cfg(feature = "embedded")]
use std::sync::OnceLock;

#[cfg(feature = "embedded")]
use design_data_core::cache;
use design_data_core::cascade::{resolve_property, ResolutionContext};
use design_data_core::diff::semantic_diff;
use design_data_core::graph::TokenGraph;
use design_data_core::primer;
use design_data_core::query;
use design_data_core::suggest;
use design_data_core::validate::relational::validate_relational;
use wasm_bindgen::prelude::*;

use crate::error::{js_err, to_js_error};
use crate::types::{
    AddedToken, DeletedToken, DeprecatedToken, DiffResult, PropertyChange, RenamedToken,
    ResolveResult, ResolutionContext as WasmContext, RevertedToken, SuggestResult,
    SuggestResultArray, TokenResult, TokenResultArray, UpdatedToken, ValidationResult,
};

// ---------------------------------------------------------------------------
// Embedded cache blob (generated at build time via `moon run sdk-wasm:cache-build`)
// ---------------------------------------------------------------------------

/// The prebuilt `.redb` cache of the canonical Spectrum dataset.
///
/// Generated by `moon run sdk-wasm:cache-build`; embedded at compile time.
/// The `OnceLock` ensures the cache is opened at most once per wasm instance.
/// Stores `Result<_, String>` so `get_or_init` (stable 1.70) can be used
/// instead of `get_or_try_init`.
#[cfg(feature = "embedded")]
static EMBEDDED_GRAPH: OnceLock<Result<TokenGraph, String>> = OnceLock::new();

// The embedded cache bytes will be set via `include_bytes!` once the codegen task
// produces the blob. The sentinel below produces a clear compile error if the blob
// is missing rather than a silent empty dataset.
//
// To generate: `moon run sdk-wasm:cache-build` (or `cargo run -p design-data-cli
// -- cache-build packages/design-data/tokens --output sdk/wasm/src/embedded_cache.redb
// --mode-sets-path packages/design-data/mode-sets
// --components-path packages/design-data/components
// --fields-path packages/design-data/fields`)
#[cfg(feature = "embedded")]
static EMBEDDED_CACHE_BYTES: &[u8] =
    include_bytes!("embedded_cache.redb");

// ---------------------------------------------------------------------------
// Dataset class
// ---------------------------------------------------------------------------

/// How this `Dataset` instance was created — used to synthesise the correct
/// provenance value in [`Dataset::primer`].
enum DatasetSource {
    /// Loaded from the embedded `.redb` blob compiled into the wasm binary.
    ///
    /// Only reachable when the `embedded` feature is enabled; suppress the
    /// dead-code lint for non-embedded builds.
    #[cfg_attr(not(feature = "embedded"), allow(dead_code))]
    Embedded,
    /// Constructed in-memory from a caller-supplied token array.
    InMemory,
}

/// A loaded token dataset you can query, validate, resolve, and diff.
///
/// ```js
/// import init, { Dataset } from "@adobe/design-data-wasm";
/// await init();
///
/// // Use the canonical embedded Spectrum dataset:
/// const ds = Dataset.embedded();
/// const results = ds.query("property=color,colorScheme=dark");
///
/// // Or load your own tokens:
/// const tokens = await fetch("/tokens.json").then(r => r.json());
/// const ds2 = Dataset.fromTokens(tokens);
/// ```
#[wasm_bindgen]
pub struct Dataset {
    graph: TokenGraph,
    source: DatasetSource,
}

#[wasm_bindgen]
impl Dataset {
    /// Open the canonical embedded Spectrum dataset from the prebuilt cache.
    ///
    /// The cache is opened once and reused; subsequent calls are cheap.
    ///
    /// Throws if the embedded cache was not generated (run `moon run sdk-wasm:cache-build`).
    #[wasm_bindgen]
    pub fn embedded() -> Result<Dataset, JsValue> {
        #[cfg(feature = "embedded")]
        {
            let graph = EMBEDDED_GRAPH
                .get_or_init(|| {
                    cache::load_from_bytes(EMBEDDED_CACHE_BYTES)
                        .map_err(|e| e.to_string())
                })
                .as_ref()
                .map_err(|e| js_err(e))?
                .clone();
            Ok(Dataset {
                graph,
                source: DatasetSource::Embedded,
            })
        }
        #[cfg(not(feature = "embedded"))]
        {
            Err(js_err(
                "Embedded cache not available: build the package with the `embedded` feature \
                 and run `moon run sdk-wasm:cache-build` to generate the .redb blob.",
            ))
        }
    }

    /// Build a dataset from an array of raw token JSON objects.
    ///
    /// Accepts the same format as the cascade token JSON files — an array of token
    /// objects each with a `name` object (e.g. `{ "name": { "property": "..." }, "value": ... }`).
    ///
    /// ```js
    /// const tokens = await fetch("/spectrum-tokens.json").then(r => r.json());
    /// const ds = Dataset.fromTokens(tokens);
    /// ```
    #[wasm_bindgen(js_name = "fromTokens")]
    pub fn from_tokens(tokens_json: JsValue) -> Result<Dataset, JsValue> {
        let tokens: Vec<serde_json::Value> =
            serde_wasm_bindgen::from_value(tokens_json).map_err(js_err)?;

        let pairs: Vec<(String, std::path::PathBuf, serde_json::Value)> = tokens
            .into_iter()
            .enumerate()
            .map(|(i, raw)| {
                // Use UUID if present as the graph key, otherwise fall back to index.
                let key = raw
                    .get("uuid")
                    .and_then(|v| v.as_str())
                    .map(String::from)
                    .unwrap_or_else(|| format!("token-{i}"));
                (key, std::path::PathBuf::from("input.json"), raw)
            })
            .collect();

        let graph = TokenGraph::from_pairs(pairs);
        Ok(Dataset {
            graph,
            source: DatasetSource::InMemory,
        })
    }

    // -----------------------------------------------------------------------
    // Primer
    // -----------------------------------------------------------------------

    /// Emit a structural overview of the dataset for agent session start.
    ///
    /// Returns a plain JS object with the same shape as `design-data primer --format json`:
    /// `{ specVersion, tokenCount, modeSets, components, taxonomyFields, manifest, provenance }`.
    ///
    /// For the embedded Spectrum dataset (`Dataset.embedded()`), `taxonomyFields` and
    /// `manifest` are baked into the prebuilt `.redb` blob by the `cache-build` task.
    /// For in-memory datasets (`Dataset.fromTokens()`), both are empty / null.
    ///
    /// `provenance.source` is:
    /// - `"embedded"` when created via `Dataset.embedded()` (includes `tokensVersion`)
    /// - `"in-memory"` when created via `Dataset.fromTokens()`
    pub fn primer(&self) -> Result<JsValue, JsValue> {
        let provenance = match self.source {
            DatasetSource::Embedded => {
                serde_json::json!({
                    "source": "embedded",
                    "tokensVersion": primer::EMBEDDED_DATA_VERSION,
                })
            }
            DatasetSource::InMemory => serde_json::json!({ "source": "in-memory" }),
        };
        let data = primer::build(&self.graph, provenance);
        // Use the json_compatible serializer so serde_json::Value::Object fields
        // (manifest, provenance) become plain JS objects rather than JS Maps.
        use serde::Serialize as _;
        let serializer = serde_wasm_bindgen::Serializer::json_compatible();
        data.serialize(&serializer).map_err(js_err)
    }

    // -----------------------------------------------------------------------
    // Query
    // -----------------------------------------------------------------------

    /// Filter tokens using a query expression.
    ///
    /// **Syntax:** `key=value` pairs separated by `,` (AND) or `|` (OR).
    /// Use `!=` for negation and `*` as a wildcard suffix or prefix.
    /// Valid keys: `property`, `component`, `variant`, `state`, `colorScheme`,
    /// `scale`, `contrast`, `uuid`, `$schema`.
    ///
    /// ```js
    /// const results = ds.query("property=color,colorScheme=dark");
    /// const all = ds.query("");   // returns all tokens
    /// ```
    #[wasm_bindgen]
    pub fn query(&self, filter_expr: &str) -> Result<TokenResultArray, JsValue> {
        let filter = query::parse(filter_expr).map_err(to_js_error)?;
        let records = query::filter(&self.graph, &filter);
        let results: Vec<TokenResult> = records.iter().map(|r| TokenResult::from(*r)).collect();
        Ok(TokenResultArray::new(results))
    }

    // -----------------------------------------------------------------------
    // Suggest
    // -----------------------------------------------------------------------

    /// Rank existing tokens by similarity to a natural-language `intent` string.
    ///
    /// Uses Jaccard similarity over the bag-of-words formed from each token's
    /// key segments, name-object field values, and description text.  Optionally
    /// filter to tokens matching a specific `propertyHint` (e.g. `"color"`).
    ///
    /// ```js
    /// const results = ds.suggest("primary button background", undefined, 5);
    /// ```
    #[wasm_bindgen]
    pub fn suggest(
        &self,
        intent: &str,
        property_hint: Option<String>,
        limit: usize,
    ) -> Result<SuggestResultArray, JsValue> {
        let results = suggest::suggest(&self.graph, intent, property_hint.as_deref(), limit);
        Ok(SuggestResultArray::new(
            results.into_iter().map(SuggestResult::from).collect(),
        ))
    }

    // -----------------------------------------------------------------------
    // Validate
    // -----------------------------------------------------------------------

    /// Run relational validation rules over the dataset.
    ///
    /// Returns a [`ValidationResult`] with `valid`, `errors`, and `warnings`.
    /// This runs the same rules as `design-data validate` but without structural
    /// (JSON Schema) checks — those require filesystem access unavailable in wasm.
    ///
    /// ```js
    /// const result = ds.validate();
    /// if (!result.valid) console.error(result.errors);
    /// ```
    #[wasm_bindgen]
    pub fn validate(&self) -> ValidationResult {
        let report = validate_relational(&self.graph, &HashSet::new(), None);
        ValidationResult::from(report)
    }

    // -----------------------------------------------------------------------
    // Resolve
    // -----------------------------------------------------------------------

    /// Resolve a token for a given property in a specific context.
    ///
    /// The `context` object maps mode-set names to their active mode values —
    /// e.g. `{ colorScheme: "dark", scale: "medium" }`.
    ///
    /// Returns the winning token record plus its specificity, or `undefined` if
    /// no token matches the property+context combination.
    ///
    /// ```js
    /// const result = ds.resolve("background", { colorScheme: "dark", scale: "medium" });
    /// if (result) console.log(result.token.raw.value);
    /// ```
    #[wasm_bindgen]
    pub fn resolve(&self, property: &str, context: WasmContext) -> Result<Option<ResolveResult>, JsValue> {
        let ctx_map = context.into_inner();

        let mut ctx = ResolutionContext::new();
        for (k, v) in ctx_map {
            ctx = ctx.with(k, v);
        }

        // Delegate to cascade::resolve_property — the same path used by cli and tui.
        // This correctly applies layer-ordering (Platform wins over Foundation) that
        // the previous inline subgraph construction did not preserve.
        match resolve_property(&self.graph, property, &ctx)
            .into_iter()
            .find(|c| c.is_winner)
        {
            Some(winner) => Ok(Some(ResolveResult {
                token: TokenResult::from(&winner.record),
                specificity: winner.specificity,
            })),
            None => Ok(None),
        }
    }

    // -----------------------------------------------------------------------
    // resolveReference — legacy-slug reference resolution (spike)
    // -----------------------------------------------------------------------

    /// Resolve a legacy token reference by name in a given context, returning
    /// the terminal value and the full alias chain.
    ///
    /// This bridges the legacy *object-map* resolution model used by
    /// `docs/s2-tokens-viewer` — where `{blue-100}` is a named slug reference
    /// — to the cascade dataset.  The wasm `resolve()` method resolves by
    /// *property* name in a mode-set context; this method resolves by *legacy
    /// slug* and a flat context map matching name-object fields.
    ///
    /// Context keys mirror cascade name-object fields, e.g.
    /// `{ colorScheme: "light" }` or `{ scale: "desktop" }`.
    /// The candidate with the most matching context fields wins.
    ///
    /// **Spike status:** this API is experimental.  The legacy slug → cascade
    /// mapping and context-field matching are sufficient for feasibility
    /// evaluation but are not production-hardened.
    ///
    /// ```js
    /// const r = ds.resolveReference("{accent-color-100}", { colorScheme: "light" });
    /// // r.chain → ["{accent-color-100}", "{blue-100}", "rgb(245, 249, 255)"]
    /// // r.value → "rgb(245, 249, 255)"
    /// ```
    #[wasm_bindgen(js_name = "resolveReference")]
    pub fn resolve_reference(
        &self,
        token_ref: &str,
        context: WasmContext,
    ) -> Result<Option<crate::types::ReferenceChainResult>, JsValue> {
        use design_data_core::naming::extract_legacy_key;

        // Strip optional `{…}` wrapper from the reference.
        let name = token_ref
            .trim()
            .trim_start_matches('{')
            .trim_end_matches('}');

        let ctx_map = context.into_inner();

        // Collect all cascade tokens whose computed legacy key equals `name`,
        // or whose direct graph key equals `name` (object-format legacy tokens).
        let candidates: Vec<&design_data_core::graph::TokenRecord> = self
            .graph
            .tokens
            .values()
            .filter(|t| {
                let legacy_match = t
                    .raw
                    .get("name")
                    .and_then(|n| extract_legacy_key(n))
                    .map(|k| k == name)
                    .unwrap_or(false);
                let direct_match = t.name == name;
                legacy_match || direct_match
            })
            .collect();

        if candidates.is_empty() {
            return Ok(None);
        }

        // Pick the candidate with the most name-object fields matching the context.
        // Tie-breaking is non-deterministic (HashMap iteration order). In practice ties
        // are rare — most slugs have one candidate per context — but this should be made
        // stable (e.g. by secondary-sorting on uuid) when this method is productionised.
        // Tracked: spectrum-design-data-nyt.
        let best = candidates
            .iter()
            .max_by_key(|t| {
                ctx_map
                    .iter()
                    .filter(|(k, v)| {
                        t.raw
                            .get("name")
                            .and_then(|n| n.get(k.as_str()))
                            .and_then(|f| f.as_str())
                            .map(|f| f == v.as_str())
                            .unwrap_or(false)
                    })
                    .count()
            })
            .copied()
            .unwrap(); // safe: candidates is non-empty

        // Build the chain by walking alias edges.
        let mut chain: Vec<String> = vec![format!("{{{}}}", name)];
        let mut current = best;
        let mut seen: std::collections::HashSet<String> = std::collections::HashSet::new();
        seen.insert(current.name.clone());

        loop {
            let Some(alias_target) = current.alias_target.as_deref() else {
                break;
            };
            let Some(next) = self.graph.resolve_alias_key(alias_target) else {
                break;
            };
            if !seen.insert(next.name.clone()) {
                break; // cycle guard
            }
            // Represent the hop as `{legacy-key}` if we can compute it.
            let hop_label = next
                .raw
                .get("name")
                .and_then(|n| extract_legacy_key(n))
                .map(|k| format!("{{{}}}", k))
                .unwrap_or_else(|| {
                    format!(
                        "(uuid:{})",
                        next.uuid.as_deref().unwrap_or("?")
                    )
                });
            chain.push(hop_label);
            current = next;
        }

        // Append the terminal value as the last chain entry.
        let value = current.raw.get("value").cloned();
        if let Some(ref v) = value {
            chain.push(match v {
                serde_json::Value::String(s) => s.clone(),
                other => other.to_string(),
            });
        }

        Ok(Some(crate::types::ReferenceChainResult { value, chain }))
    }

    // -----------------------------------------------------------------------
    // Diff
    // -----------------------------------------------------------------------

    /// Compute the semantic diff between this dataset and another.
    ///
    /// Returns a [`DiffResult`] with arrays of renamed, deprecated, reverted, added,
    /// deleted, and updated tokens.
    ///
    /// ```js
    /// const oldDs = Dataset.fromTokens(oldTokens);
    /// const newDs = Dataset.fromTokens(newTokens);
    /// const diff = oldDs.diff(newDs);
    /// console.log(`${diff.added.length} tokens added`);
    /// ```
    #[wasm_bindgen]
    pub fn diff(&self, other: &Dataset) -> Result<DiffResult, JsValue> {
        use design_data_core::diff as core_diff;
        let report = semantic_diff(&self.graph, &other.graph);

        let map_property_change =
            |c: &core_diff::PropertyChange| PropertyChange {
                path: c.path.clone(),
                change_type: format!("{:?}", c.change_type).to_lowercase(),
                new_value: c.new_value.clone(),
                original_value: c.original_value.clone(),
            };

        let renamed = report
            .renamed
            .iter()
            .map(|r| RenamedToken {
                old_name: r.old_name.clone(),
                new_name: r.new_name.clone(),
                uuid: r.uuid.clone(),
                property_changes: r.property_changes.iter().map(&map_property_change).collect(),
            })
            .collect();

        let deprecated = report
            .deprecated
            .iter()
            .map(|r| DeprecatedToken { name: r.name.clone(), uuid: r.uuid.clone() })
            .collect();

        let reverted = report
            .reverted
            .iter()
            .map(|r| RevertedToken { name: r.name.clone(), uuid: r.uuid.clone() })
            .collect();

        let added = report
            .added
            .iter()
            .map(|r| AddedToken { name: r.name.clone(), uuid: r.uuid.clone() })
            .collect();

        let deleted = report
            .deleted
            .iter()
            .map(|r| DeletedToken { name: r.name.clone(), uuid: r.uuid.clone() })
            .collect();

        let updated = report
            .updated
            .iter()
            .map(|r| UpdatedToken {
                name: r.name.clone(),
                uuid: r.uuid.clone(),
                property_changes: r.property_changes.iter().map(&map_property_change).collect(),
            })
            .collect();

        Ok(DiffResult { renamed, deprecated, reverted, added, deleted, updated })
    }

    // -----------------------------------------------------------------------
    // Introspection
    // -----------------------------------------------------------------------

    /// Return the number of tokens in the dataset.
    #[wasm_bindgen(js_name = "tokenCount")]
    pub fn token_count(&self) -> usize {
        self.graph.tokens.len()
    }
}

