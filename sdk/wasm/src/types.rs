/// The result of `Dataset.resolveReference()`.
///
/// Contains the resolved terminal value and the full chain of names/references
/// traversed to reach it — mirrors the `getResolutionChain()` behaviour from
/// the legacy inline `TokenResolver` in `docs/s2-tokens-viewer/index.html`.
///
/// ```ts
/// const r = ds.resolveReference("{accent-color-100}", { colorScheme: "light" });
/// // r.chain → ["{accent-color-100}", "{blue-100}", "rgb(245, 249, 255)"]
/// // r.value → "rgb(245, 249, 255)"
/// ```
#[derive(Debug, Clone, Serialize, Deserialize, Tsify)]
#[tsify(into_wasm_abi, from_wasm_abi)]
#[serde(rename_all = "camelCase")]
pub struct ReferenceChainResult {
    /// The resolved terminal value (absent if the chain is empty or all aliases dangle).
    #[serde(skip_serializing_if = "Option::is_none")]
    pub value: Option<serde_json::Value>,
    /// Ordered list from original reference → intermediate names → final value string.
    pub chain: Vec<String>,
}

/// A mode-set context for token resolution.
///
/// Maps mode-set names (e.g. `"colorScheme"`, `"scale"`) to their active mode
/// values (e.g. `"dark"`, `"medium"`).
///
/// ```ts
/// const result = ds.resolve("background", { colorScheme: "dark", scale: "medium" });
/// ```
#[derive(Debug, Clone, Serialize, Deserialize, Tsify)]
#[tsify(from_wasm_abi)]
pub struct ResolutionContext(std::collections::HashMap<String, String>);

impl ResolutionContext {
    pub fn into_inner(self) -> std::collections::HashMap<String, String> {
        self.0
    }
}

// ---------------------------------------------------------------------------
// Conversion helpers from core types
// ---------------------------------------------------------------------------

impl From<design_data_core::suggest::SuggestionResult> for SuggestResult {
    fn from(r: design_data_core::suggest::SuggestionResult) -> Self {
        Self {
            token_uuid: r.token_uuid,
            token_name: r.token_name,
            file: r.file.to_string_lossy().into_owned(),
            layer: format!("{:?}", r.layer).to_lowercase(),
            confidence: r.confidence,
            name_object: r.name_object,
            value: r.value,
        }
    }
}

impl From<&design_data_core::graph::TokenRecord> for TokenResult {
    fn from(r: &design_data_core::graph::TokenRecord) -> Self {
        Self {
            name: r.name.clone(),
            raw: r.raw.clone(),
            uuid: r.uuid.clone(),
            schema_url: r.schema_url.clone(),
            alias_target: r.alias_target.clone(),
            layer: format!("{:?}", r.layer).to_lowercase(),
        }
    }
}

impl From<&design_data_core::report::Severity> for Severity {
    fn from(s: &design_data_core::report::Severity) -> Self {
        match s {
            design_data_core::report::Severity::Error => Severity::Error,
            design_data_core::report::Severity::Warning => Severity::Warning,
            design_data_core::report::Severity::Info => Severity::Info,
        }
    }
}

impl From<&design_data_core::report::Diagnostic> for Diagnostic {
    fn from(d: &design_data_core::report::Diagnostic) -> Self {
        Self {
            severity: Severity::from(&d.severity),
            message: d.message.clone(),
            rule_id: d.rule_id.clone(),
            path: d.instance_path.clone().or_else(|| d.token.clone()),
        }
    }
}

impl From<design_data_core::report::ValidationReport> for ValidationResult {
    fn from(r: design_data_core::report::ValidationReport) -> Self {
        // NOTE: `ValidationReport.errors` holds ALL diagnostics regardless of severity
        // (there is no separate `warnings` field in the core type — see sdk/core/src/report.rs).
        // We split them into two JS-facing arrays by filtering the same slice twice; this is
        // intentional, not a bug.
        let errors = r
            .errors
            .iter()
            .filter(|d| {
                matches!(
                    d.severity,
                    design_data_core::report::Severity::Error
                )
            })
            .map(Diagnostic::from)
            .collect();
        let warnings = r
            .errors
            .iter()
            .filter(|d| {
                matches!(
                    d.severity,
                    design_data_core::report::Severity::Warning
                        | design_data_core::report::Severity::Info
                )
            })
            .map(Diagnostic::from)
            .collect();
        Self {
            valid: r.valid,
            errors,
            warnings,
        }
    }
}
